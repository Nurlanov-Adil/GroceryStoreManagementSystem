public abstract class StoreItem {
    protected int id;
    protected String name;

    public StoreItem(int id, String name) {
        this.id = id;
        this.name = name;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        if (name != null && !name.isEmpty()) {
            this.name = name;
        }
    }

    public abstract void showInfo();
}
